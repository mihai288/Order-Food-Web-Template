
    const parentDiv = document.getElementById('margin-page');

    const aToggle = document.getElementById('category1');
    aToggle.addEventListener('click', function(){
        const links = parentDiv.querySelectorAll('a');
        links.forEach(link => {
            link.style.background = 'white';
            link.style.color = 'black';
            link.style.transform = 'scale(1)';
        });

        document.getElementById('chicken').style.display = 'block';
        document.getElementById('beef').style.display = 'none';
        document.getElementById('menus').style.display = 'none';
        document.getElementById('christmas').style.display = 'none';
        document.getElementById('burger').style.display = 'none';
        document.getElementById('fries').style.display = 'none';
        document.getElementById('sauces').style.display = 'none';

        this.style.background = 'black';
        this.style.color = 'white';
        this.style.transform = 'scale(1.1)';
    });
